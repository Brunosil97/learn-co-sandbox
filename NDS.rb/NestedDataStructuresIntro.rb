#Intro
#In this series we're going to collaborate with the computer to discover insights buried in complex nested data structures. Complex nested data structures are things like Arrays of Arrays of Arrays or Hashes of arrays of hashes. 

#INSIGHT: There's a difference between raw data and conclusions drawn from that data. When processed correctly, data can teach us many things. The conclusions we extract from data with programming are called insights.

